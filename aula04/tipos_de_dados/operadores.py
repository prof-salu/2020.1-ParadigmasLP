a = 10
b = 3

print("soma: {0} + {1} é {2}".format(a, b, (a+b)))
print("subtração: {0} - {1} é {2}".format(a, b, (a-b)))
print("multiplicação: {0} * {1} é {2}".format(a, b, (a*b)))
print("divisao: {0} / {1} é {2}".format(a, b, (a/b)))
print("divisao inteira: {0} // {1} é {2}".format(a, b, (a//b)))
print("modulo (resto da divisao): {0} % {1} é {2}".format(a, b, (a%b)))
print("exponenciação: {0} ** {1} é {2}".format(a, b, (a**b)))